class PAM(nn.Module):
    """ Position attention module"""
    def __init__(self, query_dim, key_dim, out_dim, reduction=1):
        super(PAM, self).__init__()
        # 先经过3个卷积层生成3个新特征图B C D （尺寸不变）
        self.query_conv = nn.Conv2d(in_channels=query_dim, out_channels=out_dim // reduction, kernel_size=1)
        self.key_conv = nn.Conv2d(in_channels=key_dim, out_channels=out_dim // reduction, kernel_size=1)
        self.value_conv = nn.Conv2d(in_channels=key_dim, out_channels=out_dim, kernel_size=1)
        self.gamma = nn.Parameter(torch.ones(1))  # α尺度系数初始化为0，并逐渐地学习分配到更大的权重
        self.smooth = nn.Conv2d(in_channels=query_dim, out_channels=out_dim, kernel_size=1)
 
        self.softmax = nn.Softmax(dim=-1)  # 对每一行进行softmax
    def forward(self, x, y):
        """x from lidar, y from cam"""
        batchsize, _, height, width = x.size()
        # B -> (N,C,HW) -> (N,HW,C)
        proj_query = self.query_conv(x).view(batchsize, -1, width*height).permute(0, 2, 1)
        # C -> (N,C,HW)
        proj_key = self.key_conv(y).view(batchsize, -1, width*height)
        # BC，空间注意图 -> (N,HW,HW)
        energy = torch.bmm(proj_query, proj_key)
        # S = softmax(BC) -> (N,HW,HW)
        attention = self.softmax(energy)
        # D -> (N,C,HW)
        proj_value = self.value_conv(y).view(batchsize, -1, width*height)
        # DS -> (N,C,HW)
        out = torch.bmm(proj_value, attention.permute(0, 2, 1))  # torch.bmm表示批次矩阵乘法
        # output -> (N,C,H,W)
        out = out.view(batchsize, -1, height, width)
        out = self.gamma*out + self.smooth(x)
        torch.cuda.empty_cache()
        return out
